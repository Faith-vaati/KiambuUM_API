const CustomerLineModel = require("./NewCustomerLineConnection.model");

exports.create = (req, res) => {
  CustomerLineModel.createCustomerLine(req.body).then(
    (result) => {
      res.status(200).send(result);
    },
    (err) => {
      res.status(203).send(err);
    }
  );
};

