const WashoutsModel = require("./Washouts.model");

exports.create = (req, res) => {
  WashoutsModel.createWashout(req.body).then(
    (result) => {
      res.status(200).send(result);
    },
    (err) => {
      res.status(203).send(err);
    }
  );
};

