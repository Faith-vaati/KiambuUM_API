const SewerlinesModel = require("./Sewerlines.model");

exports.create = (req, res) => {
  SewerlinesModel.createSewerline(req.body).then(
    (result) => {
      res.status(200).send(result);
    },
    (err) => {
      res.status(203).send(err);
    }
  );
};

